package realgame;

import java.awt.Color;

public class Card {
	int number;
	Color c;
	Color fontc;
	public int getNumber() {
		return number;
	}
	
	public void setNumber(int number) {
		this.number = number;
	}
	public Color getC() {
		return c;
	}
	public void setC(Color c) {
		this.c = c;
	}
	public Color getFontc() {
		return fontc;
	}
	public void setFontc(Color fontc) {
		this.fontc = fontc;
	}
}
